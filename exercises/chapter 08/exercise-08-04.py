def triangle_area(b, h):
    return (b * h) / 2


print(f"triangle_area(6, 9) == 27 -> obtained: {triangle_area(6,9)}")
print(f"triangle_area(5, 8) == 20 -> obtained: {triangle_area(5,8)}")
